/**
 * Rule-based prediction for yield, irrigation, and crop recommendation.
 * @param {Object} params - { weather, location, size, soil }
 * @returns {Object} { predicted_yield, irrigation_recommendation, crop_recommendation, explanation }
 */
function ruleBasedPrediction({ weather, location, size, soil }) {
    // Default values and thresholds
    const cropOptions = [
        { name: 'Wheat', minN: 50, minP: 30, minK: 30, minRain: 400, maxTemp: 32, minTemp: 10, pH: [6, 7.5] },
        { name: 'Rice', minN: 60, minP: 35, minK: 40, minRain: 800, maxTemp: 38, minTemp: 18, pH: [5.5, 7] },
        { name: 'Cotton', minN: 40, minP: 25, minK: 35, minRain: 600, maxTemp: 40, minTemp: 20, pH: [6, 8] },
        { name: 'Sugarcane', minN: 80, minP: 40, minK: 60, minRain: 1000, maxTemp: 40, minTemp: 20, pH: [6, 8] }
    ];
    const { N = 0, P = 0, K = 0, pH = 7, Zn = 0, Fe = 0, Cu = 0, Mn = 0, B = 0, S = 0 } = soil || {};
    const { rainfall = 500, temperature = 25, humidity = 60 } = weather || {};
    const area = size || 1;
    let explanation = [];

    // Crop recommendation
    let suitableCrops = cropOptions.filter(crop =>
        N >= crop.minN && P >= crop.minP && K >= crop.minK &&
        rainfall >= crop.minRain &&
        temperature >= crop.minTemp && temperature <= crop.maxTemp &&
        pH >= crop.pH[0] && pH <= crop.pH[1]
    );
    let crop_recommendation = suitableCrops.length > 0 ? suitableCrops[0].name : 'Wheat';
    if (suitableCrops.length === 0) explanation.push('Defaulted to Wheat as no crop matched all criteria.');
    else explanation.push(`Recommended crop: ${crop_recommendation} based on soil NPK, rainfall, temperature, and pH.`);

    // Yield prediction (simple rule-based)
    let baseYield = 30; // quintals/acre
    if (N > 80 && P > 40 && K > 40) baseYield += 10;
    if (rainfall > 900) baseYield += 5;
    if (temperature < 15 || temperature > 35) baseYield -= 5;
    if (pH < 6 || pH > 8) baseYield -= 3;
    if (Zn < 40 || Fe < 40) baseYield -= 2;
    if (humidity > 80) baseYield -= 2;
    explanation.push(`Yield estimated from NPK, rainfall, temperature, pH, and micronutrients.`);
    let predicted_yield = Math.max(10, baseYield);

    // Irrigation recommendation
    let irrigation_recommendation = '';
    if (rainfall < 600) {
        irrigation_recommendation = 'High: Irrigate every 7-10 days.';
        explanation.push('Low rainfall detected, frequent irrigation needed.');
    } else if (rainfall < 900) {
        irrigation_recommendation = 'Moderate: Irrigate every 15 days.';
        explanation.push('Moderate rainfall, normal irrigation.');
    } else {
        irrigation_recommendation = 'Low: Rainfall sufficient, minimal irrigation needed.';
        explanation.push('High rainfall, irrigation rarely needed.');
    }

    // Adjust yield by area
    let total_yield = predicted_yield * area;
    explanation.push(`Total yield for ${area} acre(s): ${total_yield} quintals.`);

    return {
        predicted_yield: total_yield,
        irrigation_recommendation,
        crop_recommendation,
        explanation: explanation.join(' ')
    };
}